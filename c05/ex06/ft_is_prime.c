int	ft_is_prime(int nb)
{
	int	a;

	a = 2;
	if (nb < 2)
	{
		return (0);
	}
	while (nb / a >= a)
	{
		if (!(nb % a))
		{
			return (0);
		}
		else
		{
			a += 1;
		}
	}
	return (1);
}
